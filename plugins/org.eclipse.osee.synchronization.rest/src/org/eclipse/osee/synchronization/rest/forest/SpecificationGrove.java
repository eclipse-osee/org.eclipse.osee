package org.eclipse.osee.synchronization.rest.forest;

import org.eclipse.osee.orcs.data.ArtifactReadable;
import org.eclipse.osee.synchronization.rest.IdentifierType;
import org.eclipse.osee.synchronization.rest.forest.morphology.AbstractMapGrove;
import org.eclipse.osee.synchronization.util.IndentedString;

/**
 * A specialization of {@link AbstractMapGrove} for the storage of {@link SpecificationGroveThing} objects needed for a
 * Synchronization Artifact.
 * <p>
 * <table border="1" style='border-collapse:collapse'>
 * <caption>{@link SpecificationGrove}</caption>
 * <tr>
 * <th>Grove Type</th>
 * <td>{@link IdentifierType#SPECIFICATION}</td>
 * </tr>
 * <tr>
 * <th>Primary Rank</th>
 * <td>1</td>
 * </tr>
 * <tr>
 * <th rowspan="2">Primary Key Types</th>
 * <th>High Rank (index 0)</th>
 * </tr>
 * <tr>
 * <td>{@link IdentifierType.SPECIFICATION}</td>
 * </tr>
 * <tr>
 * <th>Native Rank</th>
 * <td>1</td>
 * </tr>
 * <tr>
 * <th rowspan="2">Native Key Types</th>
 * <th>High Rank (index 0)</th>
 * </tr>
 * <tr>
 * <td>{@link Long}</td>
 * </tr>
 * <tr>
 * <th>GroveThing Native Thing Classes</th>
 * <td>{@link ArtifactReadable}</td>
 * </tr>
 * </table>
 *
 * @author Loren K. Ashley
 */

public final class SpecificationGrove extends AbstractMapGrove {

   /**
    * Creates a new empty {@link SpecificationGrove}.
    */

   SpecificationGrove() {
      //@formatter:off
      super
         (
            IdentifierType.SPECIFICATION,             /* Grove Association */
            new IdentifierType[][]                    /* Allowed Primary Key Types */
               {
                 { IdentifierType.SPECIFICATION }
               },
            new Class<?>[] {Long.class}               /* Allowed Native Key Classes */
         );
      //@formatter:on
   }

   /**
    * Generates a debugging message with a listing of the {@link SpecificationGroveThing} objects stored in the grove.
    * There is no contract for the format or contents of the generated message. {@inheritDoc}
    */

   @Override
   public StringBuilder toMessage(int indent, StringBuilder message) {
      var outMessage = (message != null) ? message : new StringBuilder(1 * 1024);
      var indent0 = IndentedString.indentString(indent + 0);
      var indent1 = IndentedString.indentString(indent + 1);
      var indent2 = IndentedString.indentString(indent + 2);

      //@formatter:off
      outMessage
         .append( indent0 ).append( "SpecificationGrove:" ).append( "\n" );
      //@formatter:on

      this.streamDeep().forEach(specification -> {
         //@formatter:off
         outMessage
            .append( indent1 ).append( "SpecificationGroveThing:" ).append( "\n" )
            .append( indent2 ).append( "Key:          ").append( specification.getIdentifier() ).append( "\n" )
            .append( indent2 ).append( "Native Thing: ").append( specification.getNativeThing()   ).append( "\n" )
            ;
         //@formatter:on
      });

      return outMessage;
   }

   /**
    * Returns the message generated by the method {@link #toMessage} with an indent level of zero. {@inheritDoc}
    */

   @Override
   public String toString() {
      return this.toMessage(0, null).toString();
   }
}

