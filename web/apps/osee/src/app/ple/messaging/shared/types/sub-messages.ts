import type { difference } from '@osee/shared/types/change-report';
import type { applic } from '@osee/applicability/types';
import { attribute } from '@osee/attributes/types';
import { ATTRIBUTETYPEIDENUM } from '@osee/attributes/constants';

export type subMessage = {
	id: `${number}`;
	gammaId: `${number}`;
	name: Required<attribute<string, typeof ATTRIBUTETYPEIDENUM.NAME>>;
	description: Required<
		attribute<string, typeof ATTRIBUTETYPEIDENUM.DESCRIPTION>
	>;
	interfaceSubMessageNumber: Required<
		attribute<string, typeof ATTRIBUTETYPEIDENUM.INTERFACESUBMESSAGENUMBER>
	>;
	autogenerated?: boolean;
	applicability: applic;
};

export type subMessageWithChanges = {
	added: boolean;
	deleted: boolean;
	changes: submessageChanges;
} & subMessage;

export type submessageChanges = {
	[index: string]: difference | undefined;
	name?: difference;
	description?: difference;
	interfaceSubMessageNumber?: difference;
	applicability?: difference;
};
